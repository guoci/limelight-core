package org.yeastrc.limelight.limelight_importer_runimporter_shared.db;

/**
 * Properties File is missing or IOException while processing
 *
 */
public class DBConnectionParametersProviderPropertiesFileErrorException extends Exception {

	private static final long serialVersionUID = 1L;
	
	

	public DBConnectionParametersProviderPropertiesFileErrorException() {

	}

	public DBConnectionParametersProviderPropertiesFileErrorException(
			String message) {
		super(message);

	}

	public DBConnectionParametersProviderPropertiesFileErrorException(
			Throwable cause) {
		super(cause);

	}

	public DBConnectionParametersProviderPropertiesFileErrorException(
			String message, Throwable cause) {
		super(message, cause);

	}

	public DBConnectionParametersProviderPropertiesFileErrorException(
			String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}
	

}
